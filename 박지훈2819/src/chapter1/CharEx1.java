package chapter1;

public class CharEx1 {

	public static void main(String[] args) {
		char a = 'a';
		char b = 'b';
		char c = 'c';
		int i_a = a;
		int i_b = b;
		int i_c = c;
		char a_a = (char)i_a;
		char b_b = (char)i_b;
		char c_c = (char)i_c;

		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(i_a);
		System.out.println(i_b);
		System.out.println(i_c);
		System.out.println(a_a);
		System.out.println(b_b);
		System.out.println(c_c);
		
	}

}
